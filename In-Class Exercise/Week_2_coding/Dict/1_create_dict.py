# Creating Dictionary

my_dict = dict()
print(my_dict)

my_dict2 = {}
print(my_dict2)

eng_number = dict(one = '1', two='2', three='3')
print(eng_number)

eng_number2 = {'one':'1', 'two':'2',  'three':'3'}
print(eng_number2)

eng_number_list = [('one', '1'), ('two', '2'), ('three', '3')]
eng_number3 = dict(eng_number_list)
print(eng_number3)